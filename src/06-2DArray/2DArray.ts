const twoDArray = [
  [1, 2, 3],
  [4, 5, 6],
  [7, 8, 9],
];

// Accessing elements
console.log(twoDArray[0][0]); // Output: 1
console.log(twoDArray[0][1]); // Output: 2
console.log(twoDArray[0][2]); // Output: 3
console.log(twoDArray[1][0]); // Output: 4
console.log(twoDArray[1][1]); // Output: 5
console.log(twoDArray[1][2]); // Output: 6
console.log(twoDArray[2][0]); // Output: 7
console.log(twoDArray[2][1]); // Output: 8
console.log(twoDArray[2][2]); // Output: 9

// Modifying an element
twoDArray[1][2] = 10;
console.log(twoDArray[1][2]); // Output: 10
